#include <stdio.h>
/**
 *add - adding two integers
 *@a: first int
 *@b: second int
 *Return: addition
*/
int add(int a, int b)
{
	return (a + b);
}
/**
 *sub - subtracting two integers
 *@a: first int
 *@b: second int
 *Return: subtraction for all
*/
int sub(int a, int b)
{
	return (a - b);
}
/**
 *my_div - dividing two integers
 *@a: first int
 *@b: second int
 *Return: dividing all
*/
int my_div(int a, int b)
{
	return (a / b);
}
/**
 *mod - mod for two integers
 *@a: first int
 *@b: second int
 *Return: final result
*/
int mod(int a, int b)
{
	return (a % b);
}
#include <stdlib.h>
/**
 *mul - multiplying two integers
 *@a: first int
 *@b: second int
 *Return: multiplication
*/
int mul(int a, int b)
{
	return (a * b);
}
